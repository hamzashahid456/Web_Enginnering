			<h2><strong> <dt>Shirk</dt> </strong></h2>
			<dd>
				Associating others with God is known as shirk and is the antithesis of Tawhid. It is usually but not always in the form of idolatry and supplicating to others than Allah, or believing that they hold the same attributes as him in an equal or lesser degree. It is divided into two categories.
				
				<ul>
					<li>Greater shirk</li>
					<li>Lesser shirk</li>
				</ul>
				Greater Shirk consists of the above-mentioned deeds. A person commits lesser shirk (Shirk-al-Asghar) or hidden polytheism when he claims to believe in God but his thoughts and actions do not reflect his belief. There are also minor forms of Shirk, they must be avoided as well; these include committing a good deed to show off, making an oath in the name of anyone except God. Within Islam, shirk is an unforgivable crime; God may forgive any sin if one dies in that state except for committing shirk, repentance is required for its forgiveness.[citation needed]

